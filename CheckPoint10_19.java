public class CheckPoint10_19 {
	public static void main(String [] args) {
	/**
	 * 10.19 What is the purpose of an abstract method?
	 * 
	 * answer: The purpose of an abstract method is to declare 
	 * a method that will be used in different ways by subclasses.
	 */	
	/**
	 * 10.20 If a subclass extends a superclass with an abstract method, 
	 * what must you do in the subclass?
	 * 
	 * answer: You must override the abstract method in the subclass.
	 */
	/**
	 * 10.21 What is the purpose of an abstract class? 
	 * 
	 * answer: An abstract class serves as a general template for subclasses
	 * that will inherit from it.
	 */
	/**
	 * 10.22 If a class is defined as abstract, what can you not do with the class?
	 * 
	 * answer: You cannot instantiate an object from it.
	 */
	}
}
