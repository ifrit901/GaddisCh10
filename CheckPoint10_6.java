public class CheckPoint10_6 {
	/**	
	 * 10.6 Under what circumstances would a subclass need to 
	 * override a superclass method?
	 * 
	 * answer: When  a subclass needs to call a modified version 
	 * of the superclass method.
	 * 
	 * 10.7 How can a subclass method call an overriden superclass method?
	 * 
	 * answer: By using the super keyword. 
	 * 
	 * 10.8 If method in a subclass has the same signature as a method in 
	 * the superclass, does the subclass method overload or override the 
	 * superclass method? 
	 * 
	 * answer: If the signature is the same, then the subclass method 
	 * overrides the superclass method.
	 * 
	 * 10.9 If a method in a subclass has the same name as a method in
	 * the superclass, but uses a different parameter list, does the subclass
	 * method override or overload the superclass method?
	 * 
	 * answer: same name but different parameter list is overloading.
	 * 
	 * 10.10 How do you prevent a method from being overriden?
	 * 
	 * answer: by using the final keyword when declaring the method.
	 */
}
